import requests, re, io

s = requests.Session()
BASE = 'http://127.0.0.1:5000'

page = s.get(BASE + '/login')
pat = r'name="csrf_token" value="([^"]+)"'
m = re.search(pat, page.text)
csrf = m.group(1) if m else ''

s.post(BASE + '/login', data={'username': 'testuser', 'password': 'TestPass@12345!', 'csrf_token': csrf})

page2 = s.get(BASE + '/create_story')
m2 = re.search(pat, page2.text)
csrf2 = m2.group(1) if m2 else csrf

# Minimal valid 1x1 PNG
png = bytes([
    0x89,0x50,0x4E,0x47,0x0D,0x0A,0x1A,0x0A,
    0x00,0x00,0x00,0x0D,0x49,0x48,0x44,0x52,
    0x00,0x00,0x00,0x01,0x00,0x00,0x00,0x01,
    0x08,0x02,0x00,0x00,0x00,0x90,0x77,0x53,
    0xDE,0x00,0x00,0x00,0x0C,0x49,0x44,0x41,
    0x54,0x08,0xD7,0x63,0xF8,0xCF,0xC0,0x00,
    0x00,0x00,0x02,0x00,0x01,0xE2,0x21,0xBC,
    0x33,0x00,0x00,0x00,0x00,0x49,0x45,0x4E,
    0x44,0xAE,0x42,0x60,0x82
])

r = s.post(
    BASE + '/story/create',
    data={'caption': 'Photo story test', 'csrf_token': csrf2, 'visibility': 'Public'},
    files={'theme_video': ('test.png', io.BytesIO(png), 'image/png')},
    headers={'X-Requested-With': 'XMLHttpRequest', 'X-CSRFToken': csrf2}
)
print('Status:', r.status_code)
print('Response:', r.text[:600])
